let num1 = 3;
let num2 = 5;

console.log(num1 >= num2);
console.log(num1 <= num2); console.log("\n");

let num3 = '7'; // 문자 자료형
let num4 = 7; // 숫자 자료형

console.log(num3 == num4);
console.log(num3 === num4); console.log("\n");

console.log(num3 != num4);
console.log(num3 !== num4);

/*

1.
비교 연산식은 boolean 데이터 즉 참(true) 또는 거짓(false)를 반환한다.

2.
== 같다 (자료형이 달라도 값이 같으면 참)
!= 같지 않다
=== 완전히 같다 (자료형과 데이터가 모두 같아야 참)
!== 완전히 같지 않다.

console.log('1' == 1) // true, 자료형이 달라도 같다고 판단
console.log('1' === 1) // false, 자료형이 다르므로 다르다고 판단.

*/

